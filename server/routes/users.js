function generatePassword() {
  const chars = 'ABCDEFGHJKMNPQRSTUVWXYZabcdefghjkmnpqrstuvwxyz23456789';
  let s = '';
  const bytes = randomBytes(8);
  for (let i = 0; i < 8; i++) s += chars[bytes[i] % chars.length];
  return `Skill@${s}`;
}

// ── Balanced question picker from pre-generated bank (NO LLM calls) ───────────
function pickFromBank(bank, target = 10) {
  if (!bank?.length) return [];
  const shuffle = arr => [...arr].sort(() => Math.random() - 0.5);
  const easy   = shuffle(bank.filter(q => q.difficulty === 'easy'));
  const medium = shuffle(bank.filter(q => q.difficulty === 'medium'));
  const hard   = shuffle(bank.filter(q => q.difficulty === 'hard'));
  const n      = Math.min(target, bank.length);
  const eN = Math.round(n * 0.4);
  const mN = Math.round(n * 0.4);
  const hN = n - eN - mN;
  let picked = [...easy.slice(0, eN), ...medium.slice(0, mN), ...hard.slice(0, hN)];
  if (picked.length < n) {
    const used = new Set(picked.map(q => q.id));
    picked = [...picked, ...shuffle(bank).filter(q => !used.has(q.id))].slice(0, n);
  }
  return picked;
}

// Auto-assign a pre-assessment from the Role Library's existing question bank.
// NO API / LLM calls — uses only pre-approved questions stored in DB.
async function autoAssignPreAssessment(user) {
  if (!user.jobRole) return;
  try {
    const companyId = user.companyId || 'default';
    const roleMatch = await RoleLibrary.findByName(user.jobRole, companyId);
    const bank = roleMatch?.questionBank || [];

    if (bank.length < 5) {
      console.warn(`[auto-assessment] Role "${user.jobRole}" has no question bank (${bank.length} q) — skipping`);
      return;
    }

    const questions = pickFromBank(bank, 10);
    const assessmentId = randomUUID();
    const assessment = {
      id: assessmentId,
      title: `Pre-Assessment: ${user.jobRole}`,
      targetUsers: [user.userId],
      employeeAssignments: [{
        userId: user.userId, userName: user.name, userEmail: user.email,
        jobRole: user.jobRole, questions, status: 'assigned',
        assignedAt: new Date().toISOString(), startedAt: null, submittedAt: null,
      }],
      questionCount: questions.length, questionTypes: ['mcq'], duration: 30,
      createdBy: 'system', companyId,
      isAutoGenerated: true, autoTrigger: 'user_onboarding',
      fromRoleId: roleMatch?.id || null,
      createdAt: new Date().toISOString(), updatedAt: new Date().toISOString(), isActive: true,
    };

    await Assessments.create(assessment);
    console.log(`[auto-assessment] Assigned to ${user.email} (${user.jobRole}) — ${questions.length} questions from bank`);
  } catch (e) {
    console.error(`[auto-assessment] Failed for ${user.email}:`, e.message);
  }
}

