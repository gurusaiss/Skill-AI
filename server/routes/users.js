function normaliseRow(raw) {
  const out = {};
  for (const [k, v] of Object.entries(raw)) {
    const canon = COL_MAP[k.toLowerCase().trim()];
    if (canon) out[canon] = String(v ?? '').trim();
  }
  return out;
}

